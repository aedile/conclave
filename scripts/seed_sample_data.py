"""Sample data seeding script for the Conclave Engine E2E validation.

Generates realistic multi-table fictional data using Faker with a deterministic
seed to ensure reproducibility. All data is synthetic — no real PII is used.

Tables generated:
- customers  (id, first_name, last_name, email, ssn, phone, address, created_at)
- orders     (id, customer_id FK->customers, order_date, total_amount, status)
- order_items (id, order_id FK->orders, product_name, quantity, unit_price)
- payments   (id, order_id FK->orders, payment_date, amount, payment_method)

Usage:
    # Export CSV files to sample_data/ and print DDL to stdout (default)
    poetry run python3 scripts/seed_sample_data.py

    # Specify output directory and record counts
    poetry run python3 scripts/seed_sample_data.py \\
        --output-dir sample_data \\
        --customers 200 \\
        --orders 500 \\
        --items-per-order 3 \\
        --seed 42

    # Also execute DDL+INSERTs against a PostgreSQL database
    poetry run python3 scripts/seed_sample_data.py \\
        --dsn postgresql://conclave:conclave@localhost:5432/conclave_source

Task: P18-T18.3 — End-to-End Validation with Sample Data
CONSTITUTION Priority 0: All data is generated by Faker. No real PII is committed.
"""

