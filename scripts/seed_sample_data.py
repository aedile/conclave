def generate_customers(
    n: int = DEFAULT_CUSTOMERS,
    seed: int = DEFAULT_SEED,
) -> list[dict[str, Any]]:
    """Generate a list of fictional customer records.

    Uses a seeded Faker instance to ensure deterministic, reproducible output.
    All values are purely fictional; no real PII is included.

    Timestamps are computed as offsets from the fixed ``_EPOCH`` constant so
    that the output is identical across multiple calls with the same seed,
    regardless of wall-clock time.

    Args:
        n: Number of customer records to generate.
        seed: Deterministic seed for Faker and Python random module.

    Returns:
        A list of dicts with keys matching the customers table schema.
    """
    Faker.seed(seed)
    fake = Faker()
    rng = random.Random(seed)  # noqa: S311  # nosec B311 — PRNG for fictional data generation, not cryptographic use
    customers: list[dict[str, Any]] = []
    for i in range(1, n + 1):
        # Build a realistic timestamp spread over the last 3 years from _EPOCH.
        # Using _EPOCH (not datetime.now()) guarantees deterministic output.
        days_ago = rng.randint(0, 1095)
        created_at = _EPOCH - datetime.timedelta(days=days_ago)
        customers.append(
            {
                "id": i,
                "first_name": fake.first_name(),
                "last_name": fake.last_name(),
                "email": f"user{i}_{fake.email()}",
                "ssn": fake.ssn(),
                "phone": fake.phone_number(),
                "address": fake.address().replace("\n", ", "),
                "created_at": created_at.strftime("%Y-%m-%d %H:%M:%S"),
            }
        )
    return customers


def generate_orders(
    customers: list[dict[str, Any]],
    n: int = DEFAULT_ORDERS,
    seed: int = DEFAULT_SEED,
) -> list[dict[str, Any]]:
    """Generate a list of fictional order records referencing provided customers.

    Timestamps are computed as offsets from the fixed ``_EPOCH`` constant so
    that the output is identical across multiple calls with the same seed.

    Args:
        customers: Customer records produced by generate_customers().
                   Each order's customer_id references a customer's id.
        n: Number of order records to generate.
        seed: Deterministic seed for Faker and Python random module.

    Returns:
        A list of dicts with keys matching the orders table schema.
    """
    rng = random.Random(seed)  # noqa: S311  # nosec B311 — PRNG for fictional data generation, not cryptographic use
    customer_ids = [c["id"] for c in customers]
    orders: list[dict[str, Any]] = []
    for i in range(1, n + 1):
        days_ago = rng.randint(0, 730)
        order_date = _EPOCH - datetime.timedelta(days=days_ago)
        total_amount = round(rng.uniform(9.99, 2499.99), 2)
        orders.append(
            {
                "id": i,
                "customer_id": rng.choice(customer_ids),
                "order_date": order_date.strftime("%Y-%m-%d %H:%M:%S"),
                "total_amount": f"{total_amount:.2f}",
                "status": rng.choice(ORDER_STATUSES),
            }
        )
    return orders


def generate_order_items(
    orders: list[dict[str, Any]],
    n: int | None = None,
    seed: int = DEFAULT_SEED,
    items_per_order: int = DEFAULT_ITEMS_PER_ORDER,
) -> list[dict[str, Any]]:
    """Generate fictional order line items referencing provided orders.

    Args:
        orders: Order records produced by generate_orders(). Each item's
                order_id references an order's id.
        n: Total number of items to generate. If None, generates
           items_per_order items for each order.
        seed: Deterministic seed for random generation.
        items_per_order: Items per order when n is None.

    Returns:
        A list of dicts with keys matching the order_items table schema.
    """
    rng = random.Random(seed)  # noqa: S311  # nosec B311 — PRNG for fictional data generation, not cryptographic use
    order_ids = [o["id"] for o in orders]
    items: list[dict[str, Any]] = []

    if n is not None:
        # Generate exactly n items, randomly distributed across orders
        for i in range(1, n + 1):
            unit_price = round(rng.uniform(0.99, 499.99), 2)
            items.append(
                {
                    "id": i,
                    "order_id": rng.choice(order_ids),
                    "product_name": rng.choice(PRODUCT_NAMES),
                    "quantity": rng.randint(1, 10),
                    "unit_price": f"{unit_price:.2f}",
                }
            )
    else:
        # Generate items_per_order items for each order in sequence
        item_id = 1
        for order_id in order_ids:
            count = rng.randint(1, items_per_order * 2)
            for _ in range(count):
                unit_price = round(rng.uniform(0.99, 499.99), 2)
                items.append(
                    {
                        "id": item_id,
                        "order_id": order_id,
                        "product_name": rng.choice(PRODUCT_NAMES),
                        "quantity": rng.randint(1, 10),
                        "unit_price": f"{unit_price:.2f}",
                    }
                )
                item_id += 1

    return items


def generate_payments(
    orders: list[dict[str, Any]],
    n: int | None = None,
    seed: int = DEFAULT_SEED,
) -> list[dict[str, Any]]:
    """Generate fictional payment records referencing provided orders.

    When n is None, generates exactly one payment per order using the order's
    total_amount. When n is provided, generates exactly n payments distributed
    randomly across orders.

    Timestamps are computed as offsets from the fixed ``_EPOCH`` constant so
    that the output is identical across multiple calls with the same seed.

    Args:
        orders: Order records produced by generate_orders(). Each payment's
                order_id references an order's id.
        n: Total number of payments to generate. If None, generates exactly
           one payment per order.
        seed: Deterministic seed for random generation.

    Returns:
        A list of dicts with keys matching the payments table schema.
    """
    rng = random.Random(seed)  # noqa: S311  # nosec B311 — PRNG for fictional data generation, not cryptographic use
    order_ids = [o["id"] for o in orders]
    payments: list[dict[str, Any]] = []

    if n is not None:
        # Generate exactly n payments distributed across orders
        for i in range(1, n + 1):
            days_ago = rng.randint(0, 30)
            payment_date = _EPOCH - datetime.timedelta(days=days_ago)
            amount = round(rng.uniform(9.99, 2499.99), 2)
            payments.append(
                {
                    "id": i,
                    "order_id": rng.choice(order_ids),
                    "payment_date": payment_date.strftime("%Y-%m-%d %H:%M:%S"),
                    "amount": f"{amount:.2f}",
                    "payment_method": rng.choice(PAYMENT_METHODS),
                }
            )
    else:
        payment_id = 1
        for order in orders:
            # Every order gets at least one payment
            days_ago = rng.randint(0, 30)
            payment_date = _EPOCH - datetime.timedelta(days=days_ago)
            total = float(order["total_amount"])
            payments.append(
                {
                    "id": payment_id,
                    "order_id": order["id"],
                    "payment_date": payment_date.strftime("%Y-%m-%d %H:%M:%S"),
                    "amount": f"{total:.2f}",
                    "payment_method": rng.choice(PAYMENT_METHODS),
                }
            )
            payment_id += 1

    return payments


