def build_ddl() -> str:
    """Return PostgreSQL DDL for all four sample tables.

    Includes primary keys, foreign key references, and appropriate column
    types for the schema defined in the P18-T18.3 backlog spec.

    Returns:
        A string containing CREATE TABLE statements for all four tables.
    """
    return """\
-- Conclave Engine — Sample Data DDL
-- Generated by scripts/seed_sample_data.py (P18-T18.3)
-- All data is fictional. No real PII.

CREATE TABLE IF NOT EXISTS customers (
    id          SERIAL PRIMARY KEY,
    first_name  VARCHAR(100) NOT NULL,
    last_name   VARCHAR(100) NOT NULL,
    email       VARCHAR(255) NOT NULL UNIQUE,
    ssn         VARCHAR(11)  NOT NULL,
    phone       VARCHAR(30)  NOT NULL,
    address     TEXT         NOT NULL,
    created_at  TIMESTAMP    NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS orders (
    id            SERIAL PRIMARY KEY,
    customer_id   INTEGER      NOT NULL REFERENCES customers(id),
    order_date    TIMESTAMP    NOT NULL,
    total_amount  NUMERIC(10,2) NOT NULL,
    status        VARCHAR(20)  NOT NULL
);

CREATE TABLE IF NOT EXISTS order_items (
    id            SERIAL PRIMARY KEY,
    order_id      INTEGER      NOT NULL REFERENCES orders(id),
    product_name  VARCHAR(200) NOT NULL,
    quantity      INTEGER      NOT NULL CHECK (quantity > 0),
    unit_price    NUMERIC(10,2) NOT NULL
);

CREATE TABLE IF NOT EXISTS payments (
    id              SERIAL PRIMARY KEY,
    order_id        INTEGER      NOT NULL REFERENCES orders(id),
    payment_date    TIMESTAMP    NOT NULL,
    amount          NUMERIC(10,2) NOT NULL,
    payment_method  VARCHAR(30)  NOT NULL
);
"""


