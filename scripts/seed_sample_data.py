# Column order for each table — must match backlog spec exactly
_COLUMN_ORDER: dict[str, list[str]] = {
    "customers": [
        "id",
        "first_name",
        "last_name",
        "email",
        "ssn",
        "phone",
        "address",
        "created_at",
    ],
    "orders": ["id", "customer_id", "order_date", "total_amount", "status"],
    "order_items": ["id", "order_id", "product_name", "quantity", "unit_price"],
    "payments": ["id", "order_id", "payment_date", "amount", "payment_method"],
}


def export_csv(
    rows: list[dict[str, Any]],
    path: Path,
    table: str | None = None,
) -> None:
    """Write a list of row dicts to a CSV file.

    The CSV is written with a header row. If a table name is provided, the
    column order is taken from the canonical schema order in _COLUMN_ORDER.
    Otherwise, the order is derived from the first row's dict key order.

    Args:
        rows: List of dicts to serialise, each sharing the same key set.
        path: Destination file path. Parent directory is created if absent.
        table: Optional table name used to look up canonical column ordering.

    Raises:
        ValueError: If rows is empty and table is unknown (no columns to write).
    """
    if not rows:
        logger.warning("export_csv called with empty rows list for path=%s", path)
        return

    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames: list[str]
    if table and table in _COLUMN_ORDER:
        fieldnames = _COLUMN_ORDER[table]
    else:
        fieldnames = list(rows[0].keys())

    with path.open("w", encoding="utf-8", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    logger.info("Exported %d rows to %s", len(rows), path)


# ---------------------------------------------------------------------------
# DDL+INSERT execution (optional — requires live PostgreSQL)
# ---------------------------------------------------------------------------


def _execute_against_db(dsn: str, ddl: str, tables: dict[str, list[dict[str, Any]]]) -> None:
    """Execute DDL and INSERT statements against a PostgreSQL database.

    This function is gated behind an optional import of psycopg2 and is only
    called when --dsn is supplied. In offline/test mode, this code path is
    never executed.

    Args:
        dsn: PostgreSQL connection string.
        ddl: DDL string returned by build_ddl().
        tables: Dict mapping table name to list of row dicts.

    Raises:
        SystemExit: If psycopg2 is not importable.
        Exception: On database connection or execution errors.
    """
    try:
        import psycopg2
    except ImportError:
        logger.error("psycopg2 is not installed. Cannot connect to PostgreSQL.")
        sys.exit(1)

    logger.info("Connecting to database: %s", dsn.split("@")[-1])  # redact credentials
    conn = psycopg2.connect(dsn)
    try:
        conn.autocommit = False
        with conn.cursor() as cur:
            logger.info("Executing DDL...")
            cur.execute(ddl)
            for table_name, rows in tables.items():
                if not rows:
                    continue
                cols = _COLUMN_ORDER.get(table_name, list(rows[0].keys()))
                placeholders = ", ".join(["%s"] * len(cols))
                col_list = ", ".join(cols)
                sql = f"INSERT INTO {table_name} ({col_list}) VALUES ({placeholders})"  # noqa: S608  # nosec B608 — table_name and cols are from internal _COLUMN_ORDER dict, not user input
                values = [tuple(str(row[c]) for c in cols) for row in rows]
                cur.executemany(sql, values)
                logger.info("Inserted %d rows into %s", len(rows), table_name)
        conn.commit()
        logger.info("Transaction committed.")
    except psycopg2.Error:
        conn.rollback()
        logger.exception("Database error — transaction rolled back.")
        raise
    finally:
        conn.close()


