@click.command()
@click.option(
    "--output-dir",
    default=DEFAULT_OUTPUT_DIR,
    show_default=True,
    help="Directory for CSV exports (relative to cwd or absolute).",
)
@click.option(
    "--customers",
    "n_customers",
    default=DEFAULT_CUSTOMERS,
    show_default=True,
    type=int,
    help="Number of customer records to generate.",
)
@click.option(
    "--orders",
    "n_orders",
    default=DEFAULT_ORDERS,
    show_default=True,
    type=int,
    help="Number of order records to generate.",
)
@click.option(
    "--items-per-order",
    default=DEFAULT_ITEMS_PER_ORDER,
    show_default=True,
    type=int,
    help="Average line items per order (used when --items is not set).",
)
@click.option(
    "--seed",
    default=DEFAULT_SEED,
    show_default=True,
    type=int,
    help="Deterministic random seed for reproducibility.",
)
@click.option(
    "--dsn",
    default=None,
    help=("PostgreSQL DSN to execute DDL+INSERTs against. If omitted, only CSV files are written."),
)
@click.option(
    "--print-ddl",
    is_flag=True,
    default=False,
    help="Print DDL to stdout before exporting CSVs.",
)
def main(
    output_dir: str,
    n_customers: int,
    n_orders: int,
    items_per_order: int,
    seed: int,
    dsn: str | None,
    print_ddl: bool,
) -> None:
    """Seed sample data for Conclave Engine end-to-end validation.

    Generates fictional multi-table data using Faker (deterministic seed) and
    writes CSV exports to OUTPUT_DIR. Optionally executes DDL + INSERTs against
    a PostgreSQL database when --dsn is provided.

    All generated data is purely fictional. No real PII is produced or committed.
    """
    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s [%(levelname)s] %(name)s — %(message)s",
    )

    out = Path(output_dir)
    ddl = build_ddl()

    if print_ddl:
        click.echo(ddl)

    # --- Generate data ---
    logger.info("Generating %d customers (seed=%d)...", n_customers, seed)
    customers = generate_customers(n=n_customers, seed=seed)

    logger.info("Generating %d orders...", n_orders)
    orders = generate_orders(customers=customers, n=n_orders, seed=seed)

    logger.info("Generating order items (%d items/order avg)...", items_per_order)
    items = generate_order_items(orders=orders, seed=seed, items_per_order=items_per_order)

    logger.info("Generating payments (1 per order)...")
    payments = generate_payments(orders=orders, seed=seed)

    # --- Export CSVs ---
    export_csv(customers, out / "customers.csv", table="customers")
    export_csv(orders, out / "orders.csv", table="orders")
    export_csv(items, out / "order_items.csv", table="order_items")
    export_csv(payments, out / "payments.csv", table="payments")

    logger.info(
        "Sample data written to %s/ (%d customers, %d orders, %d items, %d payments)",
        output_dir,
        len(customers),
        len(orders),
        len(items),
        len(payments),
    )

    # --- Optional DB execution ---
    if dsn:
        tables = {
            "customers": customers,
            "orders": orders,
            "order_items": items,
            "payments": payments,
        }
        _execute_against_db(dsn=dsn, ddl=ddl, tables=tables)


if __name__ == "__main__":
    main()
