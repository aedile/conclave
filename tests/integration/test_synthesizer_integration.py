"""Integration tests for SynthesisEngine using a real CTGAN model.

These tests exercise the full synthesis pipeline:
  1. Train a CTGAN model on a Faker-generated 100-row persons DataFrame.
  2. Generate 50 synthetic rows and verify the output schema matches the
     source schema exactly (column names, dtypes, and nullable flags).
  3. Verify that the synthesizer module does NOT import from modules/ingestion/,
     modules/masking/, or modules/subsetting/ (import boundary enforcement).
  4. (T4.2c) Integration test for run_synthesis_job using a real PostgreSQL
     database: assert status == "COMPLETE" and artifact_path is set.

No real PII is used.  All fixture data is generated by Faker.

Task: P4-T4.2b — Synthesizer Core (SDV/CTGAN Integration)
Task: P4-T4.2c — Huey Task Wiring & Checkpointing (PostgreSQL integration test)
ADR: ADR-0017 (CTGAN + Opacus; per-table training with FK post-processing)
"""

from __future__ import annotations

import shutil
import subprocess
import tempfile
from pathlib import Path

import pandas as pd
import pytest
from faker import Faker
from pytest_postgresql import factories

from tests.conftest_types import PostgreSQLProc

