synth_pg_proc = factories.postgresql_proc(port=None)  # port=None picks a free port


class TestSynthesisJobTaskIntegration:
    """Integration tests for _run_synthesis_job_impl with a real PostgreSQL database.

    These tests verify the full status lifecycle (QUEUED → TRAINING → COMPLETE)
    against a real PostgreSQL instance provisioned by pytest-postgresql.
    SynthesisEngine.train() is mocked so the tests do not require sdv/torch.

    Task: P4-T4.2c — Huey Task Wiring & Checkpointing
    """

    def test_synthesis_job_complete_with_real_db(
        self,
        tmp_path: Path,
        synth_pg_proc: PostgreSQLProc,
    ) -> None:
        """_run_synthesis_job_impl must reach COMPLETE status using a real PostgreSQL DB.

        Verifies end-to-end lifecycle:
          1. A SynthesisJob row is inserted with status=QUEUED.
          2. _run_synthesis_job_impl is called with a mocked SynthesisEngine.
          3. The final DB record has status=COMPLETE and artifact_path set.

        Uses pytest-postgresql to provision an ephemeral PostgreSQL instance.
        SynthesisEngine.train() is mocked to avoid requiring sdv/torch.

        Args:
            tmp_path: pytest temporary directory for parquet and checkpoint files.
            synth_pg_proc: pytest-postgresql process executor fixture.
        """
        from unittest.mock import MagicMock, patch

        import psycopg2
        from sqlmodel import Session, SQLModel, create_engine

        from synth_engine.modules.synthesizer.jobs.job_models import SynthesisJob
        from synth_engine.modules.synthesizer.jobs.job_orchestration import _run_synthesis_job_impl

        # Build the SQLAlchemy URL from the running proc fixture.
        proc = synth_pg_proc
        dbname = "synth_test_db"

        # Create the test database (the proc fixture starts PostgreSQL but does
        # not pre-create a named database; we connect to postgres to create it).
        admin_conn = psycopg2.connect(
            dbname="postgres",
            user=proc.user,
            host=proc.host,
            port=proc.port,
            password=proc.password or "",
        )
        admin_conn.autocommit = True
        with admin_conn.cursor() as cur:
            cur.execute(f"CREATE DATABASE {dbname}")
        admin_conn.close()

        pg_url = (
            f"postgresql+psycopg2://{proc.user}:{proc.password or ''}"
            f"@{proc.host}:{proc.port}/{dbname}"
        )
        db_engine = create_engine(pg_url)
        SQLModel.metadata.create_all(db_engine)

        # Dummy parquet path — check_memory_feasibility is mocked, so the
        # file does not need to exist.  _get_parquet_dimensions falls back
        # to conservative defaults when pyarrow is absent or file missing.
        parquet_path = str(tmp_path / "test_table.parquet")

        checkpoint_dir = str(tmp_path / "checkpoints")
        Path(checkpoint_dir).mkdir()

        # Insert a QUEUED job.
        with Session(db_engine) as session:
            job = SynthesisJob(
                status="QUEUED",
                total_epochs=3,
                num_rows=100,
                checkpoint_every_n=5,
                table_name="test_table",
                parquet_path=parquet_path,
            )
            session.add(job)
            session.commit()
            session.refresh(job)
            assert job.id is not None
            job_id: int = job.id

        # Mock SynthesisEngine to avoid requiring sdv/torch.
        mock_engine = MagicMock()
        mock_artifact = MagicMock()
        mock_engine.train.return_value = mock_artifact
        mock_artifact.save.return_value = str(
            tmp_path / "checkpoints" / f"job_{job_id}_epoch_3.pkl"
        )
        with Session(db_engine) as session:
            with patch(
                "synth_engine.modules.synthesizer.jobs.job_orchestration.check_memory_feasibility"
            ):
                _run_synthesis_job_impl(
                    job_id=job_id,
                    session=session,
                    engine=mock_engine,
                    checkpoint_dir=checkpoint_dir,
                )

        # Verify final DB state in a fresh session.
        with Session(db_engine) as session:
            final_job = session.get(SynthesisJob, job_id)
            assert final_job is not None
            assert final_job.status == "COMPLETE", (
                f"Expected status=COMPLETE, got {final_job.status!r} "
                f"(error_msg={final_job.error_msg!r})"
            )
            assert final_job.artifact_path is not None, (
                "Expected artifact_path to be set after COMPLETE"
            )
