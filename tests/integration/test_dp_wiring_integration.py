class TestDPWiringBudgetExhaustion:
    """AC5: BudgetExhaustionError raised when allocated_epsilon is very low.

    Sets allocated_epsilon to an absurdly small value (e.g. 1e-10) so that
    the budget is exhausted immediately after the first gradient step.
    """

    def test_budget_exhaustion_raises_error(self, persons_parquet: str) -> None:
        """AC5: check_budget() raises BudgetExhaustionError when epsilon overspent.

        Train with dp_wrapper, then call check_budget() with a tiny
        allocated_epsilon that is guaranteed to be less than what Opacus
        spent during even a single gradient step.
        """
        from synth_engine.bootstrapper.main import build_dp_wrapper, build_synthesis_engine
        from synth_engine.modules.privacy.dp_engine import BudgetExhaustionError

        wrapper = build_dp_wrapper(max_grad_norm=1.0, noise_multiplier=1.1)
        engine = build_synthesis_engine(epochs=2)

        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            engine.train(
                table_name="persons",
                parquet_path=persons_parquet,
                dp_wrapper=wrapper,
            )

        # epsilon_spent after 2 epochs is definitely > 1e-10
        epsilon_after = wrapper.epsilon_spent(delta=1e-5)
        assert epsilon_after > 0, "Precondition: must have spent some epsilon."

        # Now check_budget with an impossibly small allocated_epsilon
        with pytest.raises(BudgetExhaustionError):
            wrapper.check_budget(allocated_epsilon=1e-10, delta=1e-5)

    def test_budget_not_exhausted_with_large_allocation(self, persons_parquet: str) -> None:
        """check_budget() must NOT raise when epsilon_spent < allocated_epsilon.

        Verifies the happy path: a generous allocated_epsilon means no exception.
        """
        from synth_engine.bootstrapper.main import build_dp_wrapper, build_synthesis_engine

        wrapper = build_dp_wrapper(max_grad_norm=1.0, noise_multiplier=1.1)
        engine = build_synthesis_engine(epochs=2)

        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            engine.train(
                table_name="persons",
                parquet_path=persons_parquet,
                dp_wrapper=wrapper,
            )

        # A very large allocated_epsilon means the budget can never be exhausted
        # in 2 epochs — this must not raise.
        wrapper.check_budget(allocated_epsilon=1_000_000.0, delta=1e-5)


