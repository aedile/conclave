class TestDPWiringEpsilonConsistency:
    """AC6: PrivacyEngine.get_epsilon(delta) consistent with training config.

    Higher noise_multiplier → stronger privacy → lower epsilon (better).
    Lower noise_multiplier → weaker privacy → higher epsilon (worse).
    """

    def test_higher_noise_multiplier_gives_lower_epsilon(self, persons_parquet: str) -> None:
        """AC6: Larger noise_multiplier must yield lower or equal epsilon_spent.

        Trains two wrappers on the same data (same epochs):
          - wrapper_noisy: noise_multiplier=2.0 (more noise → better privacy)
          - wrapper_quiet: noise_multiplier=0.5 (less noise → worse privacy)

        The noisy wrapper must have a lower epsilon than the quiet one,
        consistent with the privacy-utility tradeoff in DP-SGD.
        """
        from synth_engine.bootstrapper.main import build_dp_wrapper, build_synthesis_engine

        wrapper_noisy = build_dp_wrapper(max_grad_norm=1.0, noise_multiplier=2.0)
        wrapper_quiet = build_dp_wrapper(max_grad_norm=1.0, noise_multiplier=0.5)

        engine_noisy = build_synthesis_engine(epochs=2)
        engine_quiet = build_synthesis_engine(epochs=2)

        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            engine_noisy.train(
                table_name="persons",
                parquet_path=persons_parquet,
                dp_wrapper=wrapper_noisy,
            )

        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            engine_quiet.train(
                table_name="persons",
                parquet_path=persons_parquet,
                dp_wrapper=wrapper_quiet,
            )

        epsilon_noisy = wrapper_noisy.epsilon_spent(delta=1e-5)
        epsilon_quiet = wrapper_quiet.epsilon_spent(delta=1e-5)

        assert epsilon_noisy < epsilon_quiet, (
            f"Higher noise_multiplier must yield lower epsilon. "
            f"noisy (nm=2.0): epsilon={epsilon_noisy:.4f}, "
            f"quiet (nm=0.5): epsilon={epsilon_quiet:.4f}. "
            "Larger Gaussian noise → stronger DP guarantee → lower epsilon."
        )

    def test_epsilon_returns_float(self, persons_parquet: str) -> None:
        """AC6: epsilon_spent() must return a Python float."""
        from synth_engine.bootstrapper.main import build_dp_wrapper, build_synthesis_engine

        wrapper = build_dp_wrapper(max_grad_norm=1.0, noise_multiplier=1.1)
        engine = build_synthesis_engine(epochs=2)

        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            engine.train(
                table_name="persons",
                parquet_path=persons_parquet,
                dp_wrapper=wrapper,
            )

        epsilon = wrapper.epsilon_spent(delta=1e-5)

        assert isinstance(epsilon, float), f"epsilon_spent() must return float, got {type(epsilon)}"

    def test_epsilon_before_wrap_is_zero(self) -> None:
        """epsilon_spent() before wrap() must return 0.0.

        Verifies that the 'not-wrapped' state correctly reports 0.0 epsilon.
        This ensures no phantom epsilon is reported before training starts.
        """
        from synth_engine.modules.privacy.dp_engine import DPTrainingWrapper

        wrapper = DPTrainingWrapper()

        epsilon = wrapper.epsilon_spent(delta=1e-5)

        assert epsilon == 0.0, f"epsilon_spent() before wrap() must be 0.0, got {epsilon}"
