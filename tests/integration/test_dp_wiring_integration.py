# AC4: train with DP-SGD enabled, verify epsilon_spent > 0
# ---------------------------------------------------------------------------


class TestDPWiringEpsilonSpent:
    """AC4: epsilon_spent(delta=1e-5) > 0 after training with dp_wrapper."""

    def test_epsilon_spent_positive_after_dp_training(self, persons_parquet: str) -> None:
        """AC4: Train SynthesisEngine with build_dp_wrapper(); epsilon_spent > 0.

        This is the primary end-to-end integration test for T7.3.
        Verifies the full wiring:
          - build_dp_wrapper() constructs a DPTrainingWrapper.
          - SynthesisEngine.train(dp_wrapper=...) routes to DPCompatibleCTGAN.
          - DPCompatibleCTGAN.fit() invokes the Opacus PrivacyEngine.
          - After training, wrapper.epsilon_spent(delta=1e-5) returns a
            positive value (Opacus has tracked at least one gradient step).
        """
        from synth_engine.bootstrapper.main import build_dp_wrapper, build_synthesis_engine

        wrapper = build_dp_wrapper(max_grad_norm=1.0, noise_multiplier=1.1)
        engine = build_synthesis_engine(epochs=2)

        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            engine.train(
                table_name="persons",
                parquet_path=persons_parquet,
                dp_wrapper=wrapper,
            )

        epsilon = wrapper.epsilon_spent(delta=1e-5)

        assert epsilon > 0.0, (
            f"epsilon_spent(delta=1e-5) must be > 0 after DP training, got {epsilon}. "
            "Opacus PrivacyEngine must have been activated and at least one gradient "
            "step must have been tracked."
        )

    def test_epsilon_is_finite_after_dp_training(self, persons_parquet: str) -> None:
        """epsilon_spent must be a finite float after training.

        Guards against Opacus returning nan or inf for degenerate configs.
        """
        import math

        from synth_engine.bootstrapper.main import build_dp_wrapper, build_synthesis_engine

        wrapper = build_dp_wrapper(max_grad_norm=1.0, noise_multiplier=1.1)
        engine = build_synthesis_engine(epochs=2)

        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            engine.train(
                table_name="persons",
                parquet_path=persons_parquet,
                dp_wrapper=wrapper,
            )

        epsilon = wrapper.epsilon_spent(delta=1e-5)

        assert math.isfinite(epsilon), f"epsilon_spent must be a finite float, got {epsilon}."


